"""Lesson 3: making decisions with if/elif/else."""

battery_percent = 18

if battery_percent >= 80:
    print("Battery is high. Great for testing.")
elif battery_percent >= 20:
    print("Battery is okay, but keep the flight short.")
else:
    print("Battery is low. Do not fly yet.")


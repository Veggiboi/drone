"""Lesson 4: repeating work with for and while."""

print("For loop:")
for step in range(1, 4):
    print(f"Step {step}")

print()
print("While loop countdown:")
countdown = 3
while countdown > 0:
    print(countdown)
    countdown -= 1

print("Go!")

